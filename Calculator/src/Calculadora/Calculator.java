
package Calculadora;

public class Calculator {

    public double result;
    
    
    public void calsuma(double n1, double n2){
        result = n1 + n2;
    }
    public void calresta(double n1, double n2){
        result = n1 - n2;
    }
    public void calmultipl(double n1, double n2){
        result = n1 * n2;
    }
    public void caldiv(double n1, double n2){
        result = n1 / n2;
    }
}

